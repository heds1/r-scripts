# Principle 1: show comparisons
#   - evidence for a hypothesis is always relative to another competing
#     hypothesis
#   - always ask 'compared to what?' 
# Principle 2: show causality, mechanism,
#     explanation, systematic structure
#   - what is your causal framework for thinking about a question? 
# Principle 3:
#     show multivariate data
#   - multivariate = more than 2 variables
#   - the real world is multivariate
#   - need to 'escape flatland' 
# Principle 4: integration of evidence
#   - completely integrate words, numbers, images, diagrams
#   - data graphics should make use of many modes of data presentation
#   - don't let the tool drive the analysis (make the plot that you want to
#     make, not just what you can easily do)
# Princple 5: describate and document the evidence with appropriate labels, 
#     scales, sources, etc.
#   - a data graphic should tell a complete story that is credible
# Principle 6: content is king
#   - analytical presentations ultimately stand or fall depending on the quality,
#     relevance and integrity of their content
